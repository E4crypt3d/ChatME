def is_valid_name(word: str) -> bool:
    """Check if a word is a valid character name."""
    w = word.lower()
    return (
        len(word) >= 3
        and word[0].isupper()
        and word.isalpha()
        and w not in NAME_STOPWORDS
        and w not in RELATION_WORDS
        and w not in FALSE_NAME_WORDS
    )
